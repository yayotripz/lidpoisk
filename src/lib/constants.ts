export function getCitiesForCountries(countryKeys: string[]): string[] {
  const set = new Set<string>()
  for (const ck of countryKeys) {
    const c = COUNTRIES.find((c) => c.key === ck)
    if (c) c.cities.forEach((city) => set.add(city))
  }
  return Array.from(set)
}

