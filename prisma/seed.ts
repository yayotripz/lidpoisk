async function main() {
  console.log("Очистка старых данных...")
  await db.savedLead.deleteMany()
  await db.lead.deleteMany()
  await db.search.deleteMany()

  console.log("Генерация моковых лидов...")

  const total = 3000
  const leads: Array<{
    companyName: string
    niche: string
    country: string
    city: string
    address: string
    phone: string
    whatsapp: string | null
    telegram: string | null
    website: string | null
    rating: number
    reviewsCount: number
    problems: string
    leadScore: number
    status: string
    source: string
    companyType: string
  }> = []

  // Шаг 1: Гарантия минимального покрытия крупных городов России (население > 100к).
  // В каждый такой город кладём 5-12 лидов случайной ниши.
  console.log(`Шаг 1: гарантия покрытия ${RUSSIA_CITIES_100K_PLUS.length} крупных городов России...`)
  const usedNames = new Set<string>()

  function makeLead(country: { key: string; label: string }, city: string, nicheKey: string) {
    const nameTemplates = NAME_TEMPLATES[nicheKey] || NAME_TEMPLATES.restaurants
    let baseName = pick(nameTemplates)
    let companyName = chance(0.4) ? `${baseName} ${randInt(1, 99)}` : baseName
    // Уникализируем, если уже было
    let attempt = 0
    while (usedNames.has(`${companyName}|${city}`) && attempt < 10) {
      baseName = pick(nameTemplates)
      companyName = `${baseName} ${randInt(1, 999)}`
      attempt++
    }
    usedNames.add(`${companyName}|${city}`)

    const phone = generatePhone(country.key)
    const website = generateWebsite(companyName)
    const telegram = generateTelegram()
    const whatsapp = generateWhatsapp(phone)
    const rating = chance(0.85) ? Math.round((rand() * 2 + 3) * 10) / 10 : 0 // 3.0 - 5.0 или 0
    const reviewsCount = chance(0.9) ? randInt(1, 320) : 0
    const source = pick(DATA_SOURCES)
    const companyType = pick(COMPANY_TYPE_KEYS)
    const address = `${pick(STREETS)}, д. ${randInt(1, 200)}${chance(0.5) ? `, кв. ${randInt(1, 50)}` : ""}`

    const problems = detectProblems({
      website,
      telegram,
      whatsapp,
      rating,
      reviewsCount,
      socials: chance(0.5) ? "yes" : null,
      mapsCard: chance(0.7),
    })

    const leadScore = calculateLeadScore({
      website,
      telegram,
      whatsapp,
      rating,
      reviewsCount,
      city,
    })

    return {
      companyName,
      niche: nicheKey,
      country: country.key,
      city,
      address,
      phone,
      whatsapp,
      telegram,
      website,
      rating,
      reviewsCount,
      problems: serializeProblems(problems),
      leadScore,
      status: "new",
      source,
      companyType,
    }
  }

  const russia = COUNTRIES.find((c) => c.key === "russia")!
  // Распределение по крупным городам — даём 5-12 лидов каждому так, чтобы суммарно
  // получилось ~2000 лидов на этом шаге, остальное (1000) — на остальные города России и др. страны.
  const targetBigCities = Math.min(RUSSIA_CITIES_100K_PLUS.length, 250)
  for (let i = 0; i < targetBigCities; i++) {
    const city = RUSSIA_CITIES_100K_PLUS[i]
    // Крупные города (500к+) получают больше лидов
    const isBig = BIG_CITIES.has(city)
    const count = isBig ? randInt(10, 18) : randInt(5, 9)
    for (let j = 0; j < count; j++) {
      const niche = pick(NICHES).key
      leads.push(makeLead(russia, city, niche))
    }
  }
  console.log(`После шага 1: ${leads.length} лидов в ${targetBigCities} крупных городах РФ.`)

  // Шаг 2: добиваем до 3000 лидов, распределяя по остальным городам России и другим странам СНГ.
  // 70% оставшихся — Россия (мелкие города), 30% — другие страны СНГ.
  let remaining = total - leads.length
  console.log(`Шаг 2: добиваем ещё ${remaining} лидов по остальным городам...`)

  // Мелкие города России (без крупных, чтобы не дублировать избыточно)
  const russiaSmallCities = RUSSIA_CITIES.filter(
    (c) => !RUSSIA_CITIES_100K_PLUS.includes(c)
  )
  const otherCountries = COUNTRIES.filter((c) => c.key !== "russia")

  for (let i = 0; i < remaining; i++) {
    const isRussia = rand() < 0.7
    if (isRussia && russiaSmallCities.length > 0) {
      const city = pick(russiaSmallCities)
      const niche = pick(NICHES).key
      leads.push(makeLead(russia, city, niche))
    } else {
      const country = pick(otherCountries)
      const city = pick(country.cities)
      const niche = pick(NICHES).key
      leads.push(makeLead(country, city, niche))
    }
  }

  // Отдельно: докинем немного лидов в другие страны СНГ (по 20-40 на столицу) — для покрытия ТЗ по странам.
  for (const c of otherCountries) {
    const count = randInt(15, 30)
    for (let i = 0; i < count; i++) {
      const city = pick(c.cities)
      const niche = pick(NICHES).key
      leads.push(makeLead(c, city, niche))
    }
  }

  console.log(`Итого сгенерировано: ${leads.length} лидов.`)
  console.log(`Создаём лидов в базе (пакетная вставка)...`)

  // Пакетная вставка — пачками по 500, чтобы не упереться в лимиты SQLite
  const BATCH = 500
  for (let i = 0; i < leads.length; i += BATCH) {
    const slice = leads.slice(i, i + BATCH)
    await db.lead.createMany({ data: slice })
    console.log(`  → вставлено ${Math.min(i + BATCH, leads.length)} / ${leads.length}`)
  }

  const count = await db.lead.count()
  const citiesCount = await db.lead.findMany({
    select: { city: true },
    distinct: ["city"],
  })
  console.log(`✅ Готово! В базе ${count} лидов в ${citiesCount.length} уникальных городах.`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await db.$disconnect()
  })
